# for 루프에서 파이썬 3.X의 확장 시퀀스 할당

a, b, c = (1, 2, 3)                         # 튜플 할당
a, b, c
# (1, 2, 3)

for (a, b, c) in [(1, 2, 3), (4, 5, 6)]:    # for 루프에서 사용
    print(a, b, c)

# 1 2 3
# 4 5 6


a, *b, c = (1, 2, 3, 4)
a, b, c
# (1, [2, 3], 4)

for (a, *b, c) in [(1, 2, 3, 4), (5, 6, 7, 8)]:
    print(a, b, c)

# 1 [2, 3] 4
# 5 [6, 7] 8


for all in [(1, 2, 3, 4), (5, 6, 7, 8)]:    # 2.X에서 수동 슬라이싱
    a, b, c = all[0], all[1:3], all[3]
    print(a, b, c)

# 1 (2, 3) 4
# 5 (6, 7) 8
