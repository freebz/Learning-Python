# 리스트의 실제 동작

# 기본 리스트 연산들

len([1, 2, 3])                  # 길이
# 3
[1, 2, 3] + [4, 5, 6]           # 연결
# [1, 2, 3, 4, 5, 6]
['Ni!'] * 4                     # 반복
# ['Ni!', 'Ni!', 'Ni!', 'Ni!']


str([1, 2]) + "34"              # "[1, 2]" + "34"와 같음
# '[1, 2]34'
[1, 2] + list("34")             # [1, 2] + ["3", "4"]와 같음
# [1, 2, '3', '4']
