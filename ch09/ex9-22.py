# 한 단계 깊이로 반복해서 더하기

L = [4, 5, 6]
X = L * 4                       # [4, 5, 6] + [4, 5, 6] + ...와 같음
Y = [L] * 4                     # [L] + [L] + ... = [L, L, ...]

X
# [4, 5, 6, 4, 5, 6, 4, 5, 6, 4, 5, 6]
Y
# [[4, 5, 6], [4, 5, 6], [4, 5, 6], [4, 5, 6]]

L[1] = 0                        # Y는 영향을 받지만, X는 영향을 받지 않음
X
# [4, 5, 6, 4, 5, 6, 4, 5, 6, 4, 5, 6]
Y
# [[4, 0, 6], [4, 0, 6], [4, 0, 6], [4, 0, 6]]


L = [4, 5, 6]
Y = [list(L)] * 4               # L의 (공유) 복사를 포함
L[1] = 0
Y
# [[4, 5, 6], [4, 5, 6], [4, 5, 6], [4, 5, 6]]


Y[0][1] = 99                    # 네 개의 복사본은 여전히 같음
Y
# [[4, 99, 6], [4, 99, 6], [4, 99, 6], [4, 99, 6]]

L = [4, 5, 6]
Y = [list(L) for i in range(4)]
Y
# [[4, 5, 6], [4, 5, 6], [4, 5, 6], [4, 5, 6]]
Y[0][1] = 99
Y
# [[4, 99, 6], [4, 5, 6], [4, 5, 6], [4, 5, 6]]
