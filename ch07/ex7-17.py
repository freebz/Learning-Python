# 고급 포매팅 표현식 예제

x = 1234
res = 'integers: ...%d...%-6d...%06d' % (x, x, x)
res
# 'integers: ...1234...1234  ...001234'


x = 1.23456789
x                               # 2.7과 3.1 이전에서 더 많은 자릿수 출력
# 1.23456789

'%e | %f | %g' % (x, x, x)
# '1.234568e+00 | 1.234568 | 1.23457'

'%E' % x
# '1.234568E+00'


'%-6.2f | %05.2f | %+06.1f' % (x, x, x)
# '1.23   | 01.23 | +001.2'

'%s' % x, str(x)
# ('1.23456789', '1.23456789')


'%f, %.2f, %.*f' % (1/3.0, 1/3.0, 4, 1/3.0)
# '0.333333, 0.33, 0.3333'
